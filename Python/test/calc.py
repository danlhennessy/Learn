def add(x: int, y: int) -> int:
    """
    Add Function
    """
    return x + y


def subtract(x_num: int, y_num: int) -> int:
    """
    Subtract Function
    """
    return x_num - y_num


def multiply(x: int, y: int) -> int:
    """
    Multiply Function
    """
    return x * y


def divide(x: int, y: int) -> int:
    """
    Divide Function
    """
    return x / y


